def process_instruction(cmd):
    inst = cmd.split()
    if inst[0] == "noop":
        return 0, 1
    elif inst[0] == "addx":
        return int(inst[1]), 2


X = 1
cycle = 0
changes = [1]

for line in lines:
    x, c = process_instruction(line)
    X += x
    cycle += c
    changes.append(0)
    if c == 2:
        changes.append(x)

for i in range(1, len(changes)):
    x_pos = i % 40 - 1
    x_val = sum(changes[:i])
    if x_pos == 0:
        print()

    if x_val - 1 <= x_pos <= x_val + 1:
        print("#", end='')
    else:
        print(".", end='')
